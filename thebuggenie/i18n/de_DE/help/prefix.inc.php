Wenn Sie ein Präfix für eines Ihrer Produkte angeben, erscheint bei jedem Sachverhalt, der zu diesem Produkt angelegt wird,
dieses Präfix vor der Nummer des Sachverhaltes.
<br><br>
Sie können alles als Präfix benutzen. Zu Ihrem eigenem Nutzen sollten Sie darauf achten, dass dieses Präfix nicht zu lang wird.
<br><br>
<b>Ein Beispiel</b><br>
Nehmen wir an, dass Sie drei Produkte haben: <i>Produkt A</i>, <i>Produkt B</i> und <i>Produkt C</i>.
Sie wollen Ihren Produkten folgende Präfix geben:<br>
<ul>
<li>Produkt A hat Präfix <b>PA</b></li>
<li>Produkt B hat Präfix <b>PB</b></li>
<li>Produkt C hat Präfix <b>PC</b></li>
</ul>
Wenn der erste Sachverhalt für <i>Produkt A</i> angelegt wird erhält dieser Sachverhalt die Nummer <b>PA-1</b>.
Der nächste Sachverhalt wird <b>PA-2</b>. Der erste gemeldete Sachverhalt für <i>Produkt B</i> wird natürlich <b>PB-1</b> genannt
und der nächste wird dementsprechend nummeriert - <b>PB-2</b>.
<br><br>
Durch die Benutzung der Präfix für Ihre Produkte, können Sie ganz einfach - nur durch Betrachten der Nummer des Sachverhaltes - sehen,
welches Produkt von diesem Sachverhalt betroffen ist.
<br><br>
Falls Sie kein Präfix benutzen wollen, wird jeder Sachverhalt durchgehend nummeriert. Der erste gemeldete Sachverhalt erhält die Nummer <b>#1</b>,
der nächste somit die Nummer <b>#2</b>, unabhängig vom betroffenen Produkt. Sie können natürlich auch ein Produkt mit einem Präfix und ein Produkt
ohne ein Präfix verwenden.
