<div style="padding: 2px;"><b>Wie füge ich eine Datei zu meinem Sachverhalt hinzu?</b><br>
Wenn Sie eine Datei zu Ihrem Sachverhalt hinzufügen wollen, klicken Sie auf den "Durchsuchen" Button neben dem <b>Datei</b>-Feld.
Ein Datei-Auswahlfenster erscheint. Navigieren Sie zu der Datei, die Sie anhängen wollen und wählen Sie diese duch 
Doppelklicken oder durch einfaches Klicken und das Betätigen des OK Buttons, aus. Das Fenster verschwindet anschließend
und der Dateiname der gewählten Datei wird in dem <b>Datei</b>-Feld angezeigt.<br>
<br>
Denken Sie daran ebenfalls eine kurze Beschreibung über die angehängte Datei anzugeben.<br>
<br>
<b>Folgendes sollte beim hochladen von Dateien beachtet werden</b><br>
Es existieren einige Regeln, die Sie beachten sollten, wenn Sie Dateien hochladen:
<ul>
	<li>Dateien müssen vielleicht unter einer bestimmten Größe sein</li>
	<li>Dateien müssen vielleicht ein bestimmtes Format besitzen</li>
</ul>
Beachten Sie den Text unter dem <b>Beschreibungs</b>-Feld und über dem "Anhängen"-Button, der Sie über eventuelle 
Beschränkungen informiert.<br> 
