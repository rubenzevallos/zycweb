<div style="padding: 2px;"><b>Was ist eine "Art des Sachverhaltes"?</b><br>
Eine "Art des Sachverhaltes" beschreibt den Sachverhalt, den Sie melden. Durch die richtige Auswahl der 
"Art des Sachverhaltes" können Sie sicher gehen, dass der richtige Entwickler sofort benachrichtigt wird.
<br>
<br>
Es können unterschiedliche Arten zur Auswahl stehen, aber diese sind normalerweise ähnlich zu "Fehlermeldung", 
"Verbesserungsvorschlag", usw. 
Wählen Sie die Art, die am besten zu Ihrem Sachverhalt, den Sie gerade melden, passt.<br>
<br>
<br>
<b>Bsp: </b>Sie haben einen Fehler in der Software, die Sie gerade benutzen, gefunden,<br>
und Sie wollen es den Entwicklern melden, damit diese den Fehler beheben können.<br>
<i><u>Wählen Sie "Fehlermeldung"</u></i><br>
<br>
<b>Bsp 2:</b>Sie vermissen eine bedeutende Funktion in der Software.<br>
Sie wollen es den Entwicklern melden, damit diese die Funktion in einer zukünftigen Version hinzufügen können.<br>
<i><u>Wählen Sie "Verbesserungsvorschlag"</u></i><br>
