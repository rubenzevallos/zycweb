<b>Benutzer zu Editionen oder Komponenten zuweisen</b><br>
Auf der Übersichts-Seite der Editionen, Builds & Komponenten können Sie nach Entwicklern/Benutzern suchen.
Geben Sie die Details, nach denen Sie suchen, ein und klicken Sie anschließend auf den Namen des Benutzers wenn dieser erscheint.
Dadurch erschint eine Auswahlliste, bei der Sie auswählen können, wo Sie den Benutzer zuweisen möchten.
Sobald ein Sachverhalt für den Teil des Produktes, zu dem der Benutzer zugewiesen wurde, wird dieser Benutzer benachrichtigt.
Dasselbe passiert, wenn ein Sachverhalt aktualisiert wird.
Benutzer in dieser Liste erscheinen ebenfalls unter "Produkt Entwickler" auf der Übersichts-Seite des Produktes.
