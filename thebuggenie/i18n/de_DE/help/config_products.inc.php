<b>Einfacher Überblick über ein Produkt</b><br>
Um zu verstehen, wie ein Produkt gegliedert ist schauen wir uns ein Beispiel an - wir nennen es <i>"Mein Betriebssystem"</i>. Toll, ein Betriebssystem.<br>
<i>"Mein Betriebssystem"</i> erscheint in verschiedenen Editionen: <i>"Ultimate Edition"</i>, <i>"Home Edition"</i> und so weiter. Benutzen wir diese beiden Beispiele.<br>
<br>
<i>"Mein Betriebssystem"</i> besteht aus verschiedenen Komponenten, wie zum Beispiel <i>"Kernel"</i>, <i>"Treiber"</i>, <i>"Anwendungen"</i> und vielleicht viel mehr. Zusätzlich besitzt die <i>"Ultimate Edition"</i> noch die Komponente <i>"Extra Anwendungen"</i>, welche nicht in der <i>"Home Edition"</i> verfügbar ist. Das macht es einfacher für die Home Benutzer. Sicher. Wie auch immer - so haben Sie es eben festgelegt.<br>
<br>
Soweit alles klar? <b>Super!</b> Weiter geht's.<br>
<br>
<b>Builds</b><br>
Während Sie <i>"Mein Betriebssystem"</i> entwickeln, haben Sie das Bedürfnis hier und da verschiedene Versionen zu veröffentlichen, um zu überprüfen wie alles funktioniert. Möglicherweise haben Sie ungeduldige Kunden, die eifrig jeden Fortschritt verfolgen. Diese "Versionen" werden "Builds" genannt.<br>
<br>
Eine Build hat eine Versions-Nummer, welche Ihnen mitteilt, in welchem Zyklus diese Build veröffentlicht wurde.<br>
<br>
<b>Was wird für ein Produkt benötigt?</b><br>
Wie Sie oben sehen können bietet BUGS 2 alle diese Möglichkeiten. Folgendes wird für das Melden eines Sachverhaltes benötigt:
<ul>
	<li>Ein oder mehrere Produkte</li>
	<li>Produkte müssen mindestens eine Edition haben</li>
	<li>Die Edition muss mindestens eine Komponente haben</li>
	<li>Die Edition muss mindestens ein Release (Build) haben</li>
</ul> 
So, das ist ja alles schön und gut - aber wie stellt man das ganze Zeugs ein?<br>
