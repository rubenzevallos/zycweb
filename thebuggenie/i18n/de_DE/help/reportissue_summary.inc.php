<div style="padding: 2px;"><b>Was sollte eine Zusammenfassung enthalten?</b><br>
Eine Zusammenfassung sollte den Sachverhalt in einem kurzen Satz oder einigen Worten beschreiben.
Gute Zusammenfassungen vereinfachen die Identifizierung des Problems oder des Sachverhaltes, den Sie melden 
und helfen den Entwicklern sich auf die Lösung des Problems zu konzentrieren, anstatt herauszufinden, was Sie meinen.
<br>
<br>
<b>Einige Beispiele für <u>gute</u> Zusammenfassungen?</b>
<ul>
	<li>Programm X hängt sich auf wenn versucht wird PDF-Dateien zu öffnen</li>
	<li>Fehlermeldung erscheint nach Drücken des "Drucken"-Buttons</li>
	<li>Rechtschreibfehler im Titel auf Seite Y</li>
</ul>
<br>
<b>Einige Beipiele für <u>schlechte</u> Zusammenfassungen?</b>
<ul>
	<li>Programm X hängt sich auf</li>
	<li>Fehlermeldung erscheint die ganze Zeit!!!!!!!</li>
	<li>Das Programm ist buggy</li>
</ul>
