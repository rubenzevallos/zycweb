<b>Eine neue Build erstellen</b><br>
Builds werden auf der Einstellungs-Seite der Editionen hinzugefügt. 
Unterhalb der Details zur Edition finden Sie "Neue Build hinzufügen".
Hier geben Sie den Namen und die Versionsnummer(n) der neuen Build ein.
Jede Versionnummer die leer gelassen wird, erhält automatisch die 0.
Klicken Sie auf "Hinzufügen" und die neue Build erscheint automatisch in der Liste.
<br>
<br>
Bei den Builds können Sie verschiedene Aktionen ausführen, was durch die verschiedenen Symbole unter dem Namen der Build verdeutlicht wird.
Durch Überfahren der Symbole mit dem Mauszeiger sehen Sie eine Beschreibung zu diesen Symbolen.<br>
<br> 
<b>WICHTIG: Denken Sie daran anderen Benutzern Zugriff auf dieses Produkt zu geben.</b><br>
Der Zugriff wird der Gruppe, in der Sie sich befinden, erteilt.
Aber denken Sie daran, dass Sie anderen Gruppen ebenfalls Zugriff garantieren.
