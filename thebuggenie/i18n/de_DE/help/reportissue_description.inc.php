<div style="padding: 2px;"><b>Was sollte eine Beschreibung enthalten?</b><br>
Die Beschreibung sollte eine gute und vollständige Erklärung des Sachverhaltes, den Sie melden, geben. Die Beschreibung kann enthalten:
<ul>
	<li>warum melden Sie den Sachverhalt</li>
	<li>was passiert - wie lautet der Sachverhalt</li>
	<li>was würde das erwartete Verhalten sein, wenn der Sachverhalt nicht existieren würde</li>
</ul>
Denken Sie daran, dass Sie nie <i>zu</i> vollständig sein können. Je länger und detailierter die Beschreibung ist, um so besser.<br>
