<div style="padding: 2px;"><b>Was sollte "Wie zu reproduzieren" beinhalten?</b><br>
Dieses Feld ist normalerweise optional. Das Ausfüllen dieses Feldes hilft den Entwicklern das Problem zu identifizieren, 
indem es reproduziert wird. Wenn Sie hier detailiert erklären, wie Sie den Sachverhalt "ausgelöst" haben, können 
Entwickler das Problem einfacher diagnostizieren.
<br>
<br>
Denken Sie daran, dass Sie die Formatierungs-Leiste unterhalb des Eingabefeldes zur Beschreibung von 
"Wie zu reproduzieren" benutzen können.
<br>
<br>
<b>Haben Sie einige <u>gute</u> Beispiele von "Wie zu reproduzieren"?</b>
<ul>
	<li>Wenn ich das Menü <u>D</u>atei-&gt;<u>Ö</u>ffnen anklicke, danach Doppelklicken auf eine PDF in dem Auswahlfenster, 
		was erscheint, Programm X stürzt mit einer Fehlermeldung ab</li>
	<li>Wenn ich den "Drucken" Button in der Menüleiste (das mit dem Drucker Symbol) drücke, erscheint eine Fehlermeldung</li>
</ul>
Für "Wie zu reproduzieren" sowie für "Beschreibung des Sachverhaltes" zählt immer: Sie können nie <i>zuviel</i> erklären.
Je detaillierter desto besser.<br>
<br>
<i>Wenn Sie nichts angeben wollen, klicken Sie einfach auf "Übernehmen" auf der rechten Seite dieses Feldes.</i><br>
