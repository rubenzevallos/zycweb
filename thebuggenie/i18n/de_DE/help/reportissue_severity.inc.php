<div style="padding: 2px;"><b>Was ist eine Dringlichkeit?</b><br>
Es existieren normalerweise verschiedene "Dringlichkeits-Levels" welche den Entwicklern mitteilen, wie erst/kritisch 
Ihr Sachverhalt ist. 
There are usually different "severity levels" which tells the developers how serious/critical your issue is. 
Sie sollten bei der Auswahl des Dringlichkeits-Levels eher zurückhaltend agieren. Das bedeutet, dass ein in Ihren Augen 
dringender Sachverhalt nicht unbedingt generell dringend ist.
<br>
<br>
<b>Woher weiß ich, welches "Dringlichkeits-Level" ich wähle?</b><br>
Diese haben normalerweise selbsterklärende Namen wie zum Beispiel "Kritisch", "Gering", "Normal", usw. Schauen Sie nach
einem Namen, den Sie für passend halten.
<br>
<br>
<i>Sie sollten daran denken, dass "Kritisch" normalerweiße für Sachverhalte reserviert ist, die die generelle 
Funktionsweise der Software beeinflussen.</i><br>
<br> 
<u>Falls Sie nicht das passende "Dringlichkeits-Level" finden,</u> wählen Sie "Unbekannt" oder "Gering" - 
diese existieren normalerweise.
