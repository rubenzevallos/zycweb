<div style="padding: 2px;"><b>Welche Edition wähle ich aus?</b><br>
Ein Produkt kann in verschiedene "Editionen" aufgeteilt werden - Versionen des selben Produktes, welche unterschiedliche 
Eigenschaften haben kann. Sie sollten die Edition wählen, für die Sie den Sachverhalt melden möchten. Wenn Sie sich nicht 
sicher sind, benutzen Sie die standardmäßig eingestellte Edition, solange es nicht vollkommen verkehrt ist.
<br>  
<br>
<b>Die Edition, für die ich einen Sachverhalt melden möchte, ist nicht in der Liste</b><br>
In diesem Fall ist es scheinbar nicht möglich einen Sachverhalt für diese Edition zu melden. Falls dies der Fall sein
sollte, versuchen Sie die Entwickler zu kontaktieren - entweder durch Registrieren und anschließendem Senden einer 
Nachricht oder halten Sie Ausschau nach einem "Kontakt"-Link.<br>
