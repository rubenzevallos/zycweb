<div style="padding: 2px;"><b>Was ist eine Kategorie?</b><br>
Entwickler teilen gewöhnlicherweise ihre Sachverhalte, für statistische und administrative Zwecke, in Kategorien auf. 
Durch die richtige Auswahl der Kategorie können Sie den Entwicklern helfen den richtigen Sachverhalt schneller zu finden.
<br>
<br>
<b>Wie weiß ich, welche Kategorie ich wähle?</b><br>
Die Kategorien haben normalerweise selbserklärende Namen. Halten Sie nach einer Kategorie ausschau, die am besten zu 
Ihrem Sachverhalt passt. 
<i>Wenn Sie keine passende Kategorie finden können</i>, wählen Sie die Kategorie "Allgemein" oder "Unbekannt" - diese
sind meistens vorhanden.<br>
