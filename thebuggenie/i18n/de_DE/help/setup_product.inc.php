<b>Einrichten eines Produktes</b><br>
Bevor Sie Sachverhalte melden können, benötigen Sie mindestens ein Produkt. Oder Projekt. In BUGS 2 nennen wir die 
oberste Ebene ein <b>Produkt</b>. Das heißt nicht, dass Sie BUGS 2 für Projekte oder andere Sachen benutzen können, 
aber es ist gut zu wissen, wovon wir reden.<br>
<br>
Nun brauchen wir also ein Produkt. Falls Sie noch nicht auf dieser Seite sind, klicken Sie auf<br> 
<span style="display: float;"><img src="themes/oxygen/cfg_icon_projects.png">&nbsp;<b>Produkt verwalten</b></span>.
Hier erscheint eine Liste aller Produkte in BUGS 2 (auf die Sie Zugriff haben), und Sie können hier auch neue 
Produkte erstellen. Sie können ebenfalls einstellen, welches Produkt standardmäßig als Produkt eingestellt ist, wenn 
Sie Sachverhalte melden.<br>
<br>
Um ein neues Produkt zu erstellen, geben Sie in dem Feld <b>"Produkt hinzufügen"</b> den Produkt- oder Projektnamen ein 
und klicken Sie anschließend den <b>Hinzufügen</b>-Button mit dem Plus-Zeichen neben dem Eingabefeld. 
So einfach ist es ein Produkt zu erstellen!
<br>
<br>
<b>WICHTIGT: Denken Sie daran anderen Benutzern Zugriffsrechte auf das Produkt zu geben.</b>
<br>
Zugriff erhält automatisch die Gruppe, in der Sie sind. Aber denken Sie daran, ebenfalls anderen Gruppen und Benutzern 
Zugriff zu gewähren.
<br>
<br>
Das ist natürlich nicht alles, was Sie mit den Produkten in BUGS 2 machen können. Schauen wir uns die verschiedenen 
Einstellungen genauer an. Klicken Sie auf den Verweis 
<span style="display: float;"><img src="themes/oxygen/cfg_icon_projectsettings.png">&nbsp;<b>Information &amp; Einstellungen</b></span>.<br>
<br>
<b>Information &amp; Einstellungen</b><br>
Die ersten vier Eingabefelder sind für den Name, Beschreibung, Homepage und Dokumentation für Ihr Produkt:
<ul>
	<li><b>Beschreibung</b><br>Wird auf der Übersichts-Seite für Ihr Produkt erscheinen, welche Sie über die 
	Hauptseite erreichen.</li>
	<li><b>Homepage</b><br>Wird als Verweis uf der Übersichts-Seite erscheinen (siehe <i>Beschreibung</i>) und in 
	dem Produkt-Block auf der Hauptseite.</li>
	<li><b>Dokumentation</b><br>Wird als Verweis "Produkt Dokumentation" auf der Übersichts-Seite sowie auf der 
	Hauptseite erscheinen.</li>
</ul>
Sie haben ebenfalls die Einstellung "Präfix". Sie sollten dieses Präfix benutzen - es erleichtert vieles. Mehr Informationen über die Benutzung von 
Präfix finden Sie hier: <a href="?topic=prefix&prev_topic=setup_project"><b>Was ist ein Präfix?</b></a>
<br>
<br>
Außerdem gibt es noch weitere Produkt Einstellungen:
<ul>
	<li><b>Leitung von</b>
	<br>Wer ist für dieses Produkt verantwortlich. Dies kann ein Team oder ein Benutzer sein. Der Benutzer / das Team 
	wird bei neuen Sachverhalten benachichtigt und der Benutzer / das Team wird in dem Info Block des Produktes auf 
	der Hauptseite erscheinen.</li>
	<li><b>QS von</b><br>Siehe oben, nur für Qualitätssicherung (QS).</li>
</ul>
Sie können Entwickler zu verschiedenen Teilen Ihres Produktes zuweisen - mehr darüber finden Sie im nächsten Abschnitt
(siehe "Einrichten einer Edition und Auswählen einer Komponente")<br>
<ul>
	<li><b>Benutze Aufgaben in Sachverhalten</b>
	<br>
	Wenn "ja", können Sie Aufgaben zu Sachverhalten hinzufügen. Aufgaben können den Sachverhalt in kleinere Teile 
	aufteilen und können individuell verfolgt werden. Aufgaben bestitzen ebenfalls einen Status und eine zugewiesene 
	Person und Sie können auch zu eigenen Sachverhalten "aufsteigen", falls dies nötig wird.</li>
	<li><b>Abstimmung für Sachverhalte erlauben</b>
	<br>
	Erlaubt den Benutzern für Sachverhalte zu stimmen, wodurch die Entwickler wissen, welche Sachverhalte zu bevorzugen 
	sind.</li>
	<li><b>Zeitmessung</b>
	<br>
	Gibt an, wie die Zeit für den Sachverhalt gemessen werden soll. Sachverhalte können "geschätzte" und "vergangene" 
	Zeit haben.</li>
	<li><b>Stunden pro Tag</b>
	<br>
	Wird in Verbindung mit der vorherigen Einstellung benutzt (wenn entwas mit Tagen eingestellt wurde) - aus wieviel 
	Stunden besteht ein Tag.</li>
	<li><b>Geplanter Release</b>
	<br>
	Wählen Sie, ob es ein geplantes Release Datum existiert und wann dies sein soll. Es wird oben in der 
	Produkt-Übersicht angezeigt.</li>
	<li><b>Veröffentlicht</b>
	<br>Gibt an, ob das Produkt veröffentlicht ist oder nicht. Es erschein ebenfalls in der Produkt-Übersicht</li> 
	<li><b>Erlaube Sachverhalte zu melden.</b>
	<br>
	Ist jawohl selbsterklärend</li>
	<li><b>Benutze Builds</b>
	<br>
	Gibt an, ob Builds benutzt werden oder nicht.</li>
	<li><b>Standard Status für neue Sachverhalte</b>
	<br>
	Welchen Status neue Sachverhalte automatisch bekommen.</li>
</ul>
