<b>Einrichten eines Produktes</b><br>
Bevor Sie Sachverhalte melden können, benötigen Sie mindestens eine Edition in Ihrem Produkt. Wenn Sie mehrere 
Editionen für Ihr Produkt haben, können Sie dies hier einstellen.
<br>
<br>
Falls Sie noch nicht auf dieser Seite sind, klicken Sie auf  
<span style="display: float;"><img src="themes/oxygen/cfg_icon_projects.png">&nbsp;<b>Produkt verwalten</b></span>, 
danach klicken Sie auf
<br>
<span style="display: float;"><img src="themes/oxygen/cfg_icon_projecteditionsbuilds.png">&nbsp;<b>Editionen, Builds &amp; Komponenten</b></span>.
<br>
<br>
<b>Editions, Builds &amp; Komponenten</b><br>
Eine Liste mit all Ihren Editionen wird oben links angezeigt, und eine Liste von all Ihren Komponenten wird oben
rechts angezeigt.
<br>
<br>
<b>Eine neue Edition erstellen</b><br>
Um eine neue Edition zu erstellen, geben Sie in dem Feld "Eine Edition hinzufügen" den Namen der Edition ein
und drücken Sie den Plus-Button auf der rechten Seite um die Edition hinzuzufügen. Die Edition wird in der Liste
erscheinen - klicken Sie auf den Namen. Auf der neuen Seite, auf die Sie gelangen, können Sie Informationen der 
Edition bearbeiten, welche als Information für das Produkt angezeigt werden. Die Edition hat ebenfalls Einstellungen, 
welche die selben sind, wie die vom Produkt. (Lesen Sie mehr über diese Einstellungen unter "Einrichten eines Produktes")
<br>
<br>
<b>WICHTIG: Denken Sie daran anderen Benutzern Zugriffsrechte auf das Produkt zu geben.</b>
<br>
Zugriff erhält automatisch die Gruppe, in der Sie sind. Aber denken Sie daran, ebenfalls anderen Gruppen und Benutzern 
Zugriff zu gewähren.
<br>
<br>
<b>Eine neue Komponente erstellen und diese einer Edition zuweisen</b><br>
Es gibt zwei Möglichkeiten um Komponenten zu erstellen. Sie können diese direkt von der Übersichts-Seite der 
Editionen und Komponenten, die Sie am Anfang dieses Themas finden, erstellen oder auf dr Einstellungs-Seite der 
Edition. Durch das Hinzufügen einer Komponente auf der Einstellungs-Seite der Edition, wird diese Komponente 
automatisch zur edition hinzugefügt. Wenn Sie diese auf der Übersichts-Seite der Editionen und Komponenten 
hinzufügen, müssen Sie die Komponente noch zur Edition hinzufügen, bevor Sie für Benutzer verfügbar ist.
Sie können diese auf der Einstellungs-Seite der Edition, durch Klicken auf "Hinzufügen" neben dem
Namen der Komponente hinzufügen. (Sie gelangen durch Klicken auf den Namen der Edition innerhalb der Übersichts-Seite
der Editionen und Komponenten auf die Einstellungs-Seite der Edition.)
<br>
<br> 
<b>WICHTIG: Denken Sie daran anderen Benutzern Zugriff auf das Produkt zu gewähren.</b>
<br>
Zugriff erhält automatisch die Gruppe, in der Sie sind. Aber denken Sie daran, ebenfalls anderen Gruppen und Benutzern
Zugriff zu gewähren.
