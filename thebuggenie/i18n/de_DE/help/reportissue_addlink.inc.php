<div style="padding: 2px;"><b>Wie füge ich einen Verweis hinzu?</b><br>
Wenn Sie einen Verweis zu einer Internet-Seite oder einen anderen Verweis zum Sachverhalt hinzufügen wollen, 
fügen Sie die URL (kopieren Sie diese aus dem Adressfeld Ihres Browsers) in das <b>URL</b>-Feld, 
und geben Sie eine kurze Beschreibung, wohin dieser Verweis zeigt.
Wenn Sie keine Beschreibung angeben, wird stattdessen die URL benutzt.<br>
