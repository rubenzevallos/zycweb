<div style="padding: 2px;"><b>Was ist eine Komponente?</b><br>
Eine Software wird normalerweise in unterschiedliche "Teile", sogenannte Komponenten, aufgeteilt. Durch das 
Aufteilen der Software in diese Komponenten, können Entwickler oder ein Team von Entwicklern sich auf einen bestimmten 
Teil der Software konzentrieren, während andere Entwickler sich auf andere Teile konzentrieren können.
<br>
<br>
Wenn Sie die richtige Komponente auswählen, erhöht sich die Wahrscheinlichkeit, dass der richtige Entwickler sofort 
benachrichtigt wird. Somit erhöht sich auch die Chance, dass der Sachverhalt richtig bearbeitet wird.<br> 
<br>
<b>Wie weiß ich, welche Komponente ich wähle?</b><br>
Die Komponenten haben normalerweise selbsterklärende Namen, wie "Menü", "Drucken", usw - dies hängt natürlich davon ab, 
welche Art von Software Sie benutzen und kann somit alles sein. Schauen Sie nach einer Komponente, die Ihrer Meinung 
nach die passende für Ihren Sachverhalt ist.
<i>Wenn Sie keine passende Komponente finden können</i>, wählen Sie die Komponente "Allgemein" oder "Unbekannt" 
- diese sind meistens vorhanden.<br>
