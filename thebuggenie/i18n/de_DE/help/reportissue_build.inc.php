<div style="padding: 2px;"><b>Was ist eine "Build"?</b><br>
Während der Entwicklung eines Produktes können Sie immer neuere "Builds" veröffentlichen. Eine "Build" ist ein Release
zu einer gegebenen Zeit und jede "Build" ist über eine bestimmte "Versionsnummer" zu identifizieren.<br>
<br>
Sie haben dies sicherlich schon bei Programmen gesehen, die Sie selber benutzen - Versionsnummern sind zum Beispiel 
"1.1", "2.1.3", usw. Diese Information ist gewöhnlich unter dem Programm-Menü "Über" verfügbar.<br>
<br>
<b>Bsp.:</b> Wenn Sie auf den "Über"-Link auf dieser Seite klicken, dann sehen Sie welche "Build" dieses Programm hat.
(Wenn Sie drauf klicken können Sie anschließend auf "Meldung fortsetzen" klicken um auf die "Sachverhalt melden"-Seite 
zurückzukehren.)<br>  
<br>
<b>Welche Build wähle ich aus?</b><br>
Versuchen Sie die selbe "Build"-Nummer wie die in der Anwendung, die Sie benutzen. Versuchen Sie anhand der Informationen,
die Sie auf dieser Seite sehen, die "Build"-Nummer herauszufinden. Falls Sie sich nicht sicher sind, welche "Build" Sie,
haben  jedoch sicher sind, dass Sie die aktuelle Version benutzen, benutzen Sie die Standardeinstellung.<br>
<br>
<b>Die Build, für die ich einen Sachbverhalt melden möchte, ist nicht in der Liste</b><br>
In diesem Fall ist es nicht möglich einen Sachverhalt für diese Build zu melden. Falls dies der Fall sein sollte, 
versuchen Sie die Entwickler zu kontaktieren (entweder auf dieser Seite registrieren und Ihnen anschließend eine Nachricht
schicken oder schauen Sie nach einem "Kontakt"-Link.<br>
