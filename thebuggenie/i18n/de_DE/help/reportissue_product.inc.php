<div style="padding: 2px;"><b>Welches Produkt wähle ich aus?</b><br>
Das "Produkt" auf dieser Seite ist das Produkt, welches Sie gerade benutzen und für das Sie einen Sachverhalt melden.<br>
<br>
<b>"Produkt X" ist bereits ausgewählt, aber ich melde ein Sachverhalt für "Produkt Y"?</b><br>
Die Entwickler habe möglicherwiese ein Produkt standardmäßig eingestellt, dass nicht das ist, für das Sie ein 
Sachverhalt melden wollen. Um das zu ändern, klicken Sie auf den Verweis "Ändern" unter dem Namen des Produktes und
wählen Sie das richtige Produkt.
<br>
<br>
<b>Das Produkt, für das ich einen Sachverhalt melden möchte, ist nicht in der Liste</b><br>
In diesem Fall ist es nicht möglich ein Sachverhalt für dieses Produkt zu melden. Falls dies eintreten sollte
versuchen Sie die Entwickler zu kontaktieren - entweder durch Registrieren und anschließendem Senden einer Nachricht
oder schauen Sie nach einem Verweis "Kontakt".<br>
