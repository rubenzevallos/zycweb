<b>Assigning users to editions or comonents</b><br>
On the edition and components overview page, you can search for developers/users. Enter the details to search for, then click the users name when it 
appears. This will give you a drop-down menu, where you can choose where to assign the user. Whenever an issue is posted for the part of the project
where the user is assigned, the user will be notified. Same goes for when an issue is updated. Users in this list will also appear in the "project developers"
list on the project overview page.
