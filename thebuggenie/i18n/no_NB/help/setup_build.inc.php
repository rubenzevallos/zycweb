<b>Creating a new build</b><br>
Builds are added on the edition settings page. Below the edition information fields, you have an "Add new build" area. This is where you enter the build
name and the build version number(s). Any version number that is emmitted, will just be a 0. Click the "Add" button next to the fields, and the build
will appear in the list.<br>
<br>
The builds have several actions you can perform, indicated by several icons underneath the build name. By hovering over the icons, you can see the 
action description next to the icons.<br>
<br> 
<b>IMPORTANT: Remember to give other users access to the project.</b><br>Access will be given to the group you are in, but remember to grant access to
other groups or users as well.<br>
