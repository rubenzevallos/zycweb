<b>Setting up a project</b><br>
Before you can report issues, you need to have at least one project. Or project. In BUGS 2, we call the top level a <b>project</b>. Doesn't mean 
you cannot use BUGS 2 for projects, and other things - but it's nice to know what we're talking about.<br>
<br>
So, you need a project. If you're not there already, click the <span style="display: float;"><img src="themes/oxygen/cfg_icon_projects.png">&nbsp;<b>Manage projects</b></span> link.
Here, you have a list of all the projects in BUGS 2 (that you have access to), and you can create new projects. You can also set which project should be 
the default project for users when they report new issues.<br>
<br>
To create a new project, enter the project or project name in the <b>"ADD A PROJECT"</b>-field, then click <b>Add</b>-button with the plus-sign next 
to it. That's all you need to create a project!<br>
<br>
<b>IMPORTANT: Remember to give other users access to the project.</b><br>Access will be given to the group you are in, but remember to grant access to
other groups or users as well.<br>
<br>
Of course, that's not all the can be done with produts in BUGS 2. Let's look at the different settings. Click the <br>
<span style="display: float;"><img src="themes/oxygen/cfg_icon_projectsettings.png">&nbsp;<b>Information &amp; settings</b></span> link.<br>
<br>
<b>Information &amp; settings</b><br>
The first four input fields is for the name, description, homepage and documentation homepage for your project:
<ul>
	<li><b>Description</b><br>Will appear on the overview page for your project, which will be available from the frontpage</li>
	<li><b>Homepage</b><br>Will appear as a link on the overview page (see <i>description</i>) and in the project block on the frontpage</li>
	<li><b>Documentation</b><br>Will appear as a "View documentation"-link on the project overview page, and on the frontpage</li>
</ul>
You also have a setting for "prefix". You should use prefixes - they make your life easier. More information about using a project prefix is found
here: <a href="?topic=prefix&prev_topic=setup_project"><b>What is a prefix?</b></a><br>
<br>
There are also several project settings:
<ul>
	<li><b>Lead by</b><br>Who's in charge of this project. Can be a team or a user. The user/users in the team will be notified of new issues, and the
	user/team will appear on the frontpage in the info block for this project.</li>
	<li><b>QA by</b><br>Same as above, only for QA</li>
</ul>
You can also assign developers to different part of your project - more about that in the next section (see "setting up editions and components")<br>
<ul>
	<li><b>Use tasks in issue reports</b><br>If "yes", you can add tasks to issue reports. Tasks can split the issue in to smaller parts, and can be
	tracked individually. Tasks have statuses and assignees, and they can also be promoted to separate issues if they turn out to be that</li>
	<li><b>Allow voting for issues</b><br>Lets people post votes on issues, letting developers know which issues to prioritise</li>
	<li><b>Time measuring</b><br>Setting for how time should be measured on issues. Issues can have "estimated" and "elapsed" time.</li>
	<li><b>Hours per day</b><br>Used in conjunction with the previous setting when this is set to something including days - how many hours should be
	included in each day</li>
	<li><b>Release date</b><br>Select if there is a planned release date, and when it is. Will be displayed in the top of the project overview</li>
	<li><b>Released</b><br>Whether the project is released or not. Displayed same as above</li> 
	<li><b>Allow issues to be reported</b><br>Does what it says</li>
	<li><b>Use builds</b><br>Whether or not to use builds</li>
	<li><b>Default status for new issues</b><br>Which status you want new issues to have by default</li>
</ul>
