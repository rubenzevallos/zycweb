<div style="padding: 2px;"><b>What is a "build"?</b><br>
As the project evolves, newer "builds" may be released. A "build" is a release at a given time, and each "build" is 
identified by a corresponding "version number".<br>
<br>
You may have seen this in software you are using - version numbers are such as "1.1", "2.1.3", etc. This information
is usually available in the programs "about"-menu.<br>
<br>
<b>Ex.:</b> If you click the "About"-link on this page, you can see which "build" this program has. (If you click it, 
you can click the "Continue reporting" link to get back to the reporting page)<br>  
<br>
<b>What build do I select?</b><br>
Try to select the same build number as the application you are using. Use the information on this page to find out how
to find the build number. If you are unsure, but you know you have the latest version, just use the default.<br>
<br>
<b>The build I want to report an issue for is not on the list</b><br>
In this case, it might not be possible to report an issue for that build. If so, try contacting the developers, either
by registering on the page and sending them a message, or look for a "contact us" link.<br>
