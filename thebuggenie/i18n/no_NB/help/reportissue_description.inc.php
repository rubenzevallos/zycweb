<div style="padding: 2px;"><b>What should a description contain?</b><br>
The description should give a good and thorough explanation of the issue you are reporting. Things to include in the description can be:
<ul>
	<li>why you are reporting the issue</li>
	<li>what happens - what's the issue</li>
	<li>what would be the expected behaviour if the issue didn't exist</li>
</ul>
Remember that you can usually not be <i>too</i> thorough - the longer and more detailed the description is, the better.<br>
Also remember that you can use the formatting toolbar below the text field to help you describe the reproduction steps.<br>
