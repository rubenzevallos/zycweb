<div style="padding: 2px;"><b>How do I add a link to my report?</b><br>
If you want to include a link to a webpage or any other link with your issue report, paste the URL (copy it from the address bar in your 
browser, for instance) into the <b>URL</b>-field, and then write a short description about what the URL links to. If you do not provide 
a description, the URL will be used instead.<br>
