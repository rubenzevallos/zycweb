<div style="padding: 2px;"><b>What is a "severity"?</b><br>
There are usually different "severity levels" which tells the developers how serious/critical your issue is. You should be conservative when selecting
a severity level - what seems serious to you might not be so serious after all, and may even have an easy and pretty obvious workaround.<br>
<br>
<b>How do I know which "severity level" to select?</b><br>
They normally have self-explanatory names, such as "Critical", "Major", "Minor", etc. Look for a name that looks like it fits your issue.<br>
<br>
<i>You should keep in mind that "Critical" is usually reserved for issue that will stop the whole software from functioning properly or at all, and "Major"
is usually reserved for issues that have a major impact on the way the software works.</i><br>
<br> 
<u>If you cannot find the correct severity level</u>, there is usually an "unknown" level or a "minor" level - pick that one.<br>
