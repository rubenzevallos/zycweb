<div style="padding: 2px;"><b>What is an issue type?</b><br>
An "issue type" is what describes the issue you are reporting. By selecting an issue type that fits what you are reporting, 
you can be sure that the developers will be notified correctly.<br>
<br>
There can be several issue types available to choose from, but they are usually something similar to "Bug report", "Feature request", etc. 
Choose the one that best fits the issue you are currently reporting.<br>
<br>
<br>
<b>Ex: </b>You have found a flaw/bug in the software you are using,<br>and you want to report it to the developers so they can fix it.<br>
<i><u>Choose "Bug report"</u></i><br>
<br>
<b>Ex 2:</b>You are missing one important feature in the software.<br>You want to report this to the developers so they can add it in an upcoming version.<br>
<i><u>Choose "Feature request"</u></i><br>
