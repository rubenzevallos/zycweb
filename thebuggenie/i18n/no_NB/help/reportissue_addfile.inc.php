<div style="padding: 2px;"><b>How do I add a file to my report?</b><br>
If you want to include a file with your issue report, click the "Browse"-button next to the <b>File</b>-field. A file browser will pop up. Navigate to
the file you want to include, and select it by double-clicking on it, or single-click it and then press ENTER. The dialog will then disappear, and the
filename will be displayed in the <b>File</b>-field.<br>
<br>
Remember to also fill in a short description about the file you are attaching.<br>
<br>
<b>Things to remember when uploading a file</b><br>
There are some rules that you should be aware of when uploading files:
<ul>
	<li>Files may need to be below a certain size</li>
	<li>Files may need to be of a certain type</li>
</ul>
Look for the text that says anything about these restrictions, below the <b>Description</b>-field, above the "Attach"-button.<br> 
