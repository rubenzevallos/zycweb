<div style="padding: 2px;"><b>What is a "category"?</b><br>
Developers usually like to split issues into categories, for statistical and administrative reasons. 
By selecting the correct category, you can help developers find your issue faster.<br>
<br>
<b>How do I know which category to select?</b><br>
The categories usually have self-explanatory names. Look for a category that looks like it fits your issue. 
<i>If you cannot find the correct category</i>, there is usually a "general" or "unknown" category - pick that one.<br>
