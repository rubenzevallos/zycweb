<div style="padding: 2px;"><b>What project do I select?</b><br>
The "project" in this screen is the project you are using, and that you are reporting an issue for.<br>
<br>
<b>"Project X" is selected already, but I'm reporting an issue for "Project Y"?</b><br>
The developers might have set a default project that may not be the one you are reporting an issue for. To change this,
click the "Change" link below the project name, and select the correct project.<br>
<br>
<b>The project I want to report an issue for is not on the list</b><br>
In this case, it might not be possible to report an issue for that project. If so, try contacting the developers, either
by registering on the page and sending them a message, or look for a "contact us" link.<br>
