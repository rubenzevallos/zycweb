<div style="padding: 2px;"><b>What should "reproduction steps" contain?</b><br>
This field is usually optional. Filling out this field helps developers identify your problem, by being able to reproduce it. If you put in details here
about how you managed to "trigger" the issue you are reporting, developers can find and identify the issue easier.<br>
<br>
Remember that you can use the formatting toolbar below the text field to help you describe the reproduction steps.<br>
<br>
<b>Do you have some examples of <u>good</u> "reproduction steps"?</b>
<ul>
	<li>When I click the <u>F</u>ile-&gt;<u>O</u>pen menu item, then double-click a PDF in the browser that appears, Program X crashes with an error message</li>
	<li>When I press the "print" button in the menu bar (the one with the printer icon), an error message pops up</li>
	<li>Open program X, click "help", then search for "candy". Open the second item in the list ("candy bars"), then notice the spelling error in the header for the second paragraph</li>
</ul>
The same thing goes for reproduction steps as for issue descriptions - they can never be <i>too</i> thorough.<br>
<br>
<i>If you don't want to enter anything in this field, just click the "Set" button to the right of the field.</i><br>
