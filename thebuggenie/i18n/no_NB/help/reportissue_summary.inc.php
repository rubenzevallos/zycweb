<div style="padding: 2px;"><b>What should an issue summary contain?</b><br>
The issue summary should sum up your issue in a short sentence or a few words. Good summaries makes it easy to identify the problem or issue you
are reporting and helps developers concentrate on fixing the problem instead of having to figure out what you mean.<br>
<br>
<b>Do you have some examples of <u>good</u> issue summaries?</b>
<ul>
	<li>Program X dies when trying to open PDF-files</li>
	<li>Error message appears when pressing "print"-button</li>
	<li>Spelling error in title on page Y</li>
</ul>
<br>
<b>Do you have some examples of <u>bad</u> issue summaries?</b>
<ul>
	<li>Program X crashes</li>
	<li>Error message pops up all the time!!!!!!!!!!!!!</li>
	<li>This program is buggy</li>
</ul>
